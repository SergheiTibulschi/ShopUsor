import Foundation
import SwiftUI

extension Font {
    static func montserratBold(size: CGFloat) -> Font {
        return Font.custom("Montserrat-Bold", size: 700)
    }
    
    static func montserratRegular(size: CGFloat) -> Font {
        return Font.custom("Montserrat-Regular", size: 400)
    }
    
}
